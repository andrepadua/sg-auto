    private void PreencherTabela()
            throws Exception {
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("Código");
        dtm.addColumn("Potência");
        dtm.addColumn("Vel. Final");
        dtm.addColumn("Autonomia");
        dtm.addColumn("Descrição");
        dtm.addColumn("Tipo Motorização");
        try {
            ArrayList<Motorizacao> lista = mBus.Consultar();
            for (Motorizacao m : lista) {
                dtm.addRow(new String[]{
                    String.valueOf(m.getSEQ_MOTORIZACAO()),
                    String.valueOf(m.getNUM_POTENCIA()),
                    String.valueOf(m.getNUM_VELOCIDADE_FINAL()),
                    String.valueOf(m.getNUM_AUTONOMIA()),
                    m.getDSC_MOTORIZACAO(),
                    m.getDESC_TIPOMOTORIZACAO()
                });
            }
            tblResult.setModel(dtm);
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

    private void pCheckMenu() {
        if (frmLogin.PerfUsu != 1) {
            btnCadastrar.setEnabled(false);
            btnAtualizar.setEnabled(false);
            btnExcluir.setEnabled(false);
        }
    }

    private void PreencherCombo() throws ClassNotFoundException {
        DefaultComboBoxModel modelPerfil = new DefaultComboBoxModel();
        ArrayList<TipoMotorizacao> lista = tmBus.Consultar();
        for (TipoMotorizacao tm : lista) {
            modelPerfil.addElement(tm.getDESC_TIPOMOTORIZACAO());
        }
        cboTM.removeAllItems();
        cboTM.setModel(modelPerfil);
    }

