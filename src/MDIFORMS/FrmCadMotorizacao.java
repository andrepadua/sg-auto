    private int fc_verif_campos() {
        if (txtAut.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Não possui Autonomia. Favor Preecher!");
            return 0;
        }

        if (txtNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Não possui Descrição. Favor Preecher!");
            return 0;
        }

        if (txtPot.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Não possui Potência. Favor Preecher!");
            return 0;
        }

        if (txtVF.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Não possui Velcidade Final. Favor Preecher!");
            return 0;
        }

        return 1;
    }

