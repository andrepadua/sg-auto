    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if (fc_verif_campos() == 1) {
            int Seq = 0;
            int i = 0;
            try {
                i = tmBus.fc_get_seq(cboTM.getSelectedItem().toString());
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(FrmCadMotorizacao.class.getName()).log(Level.SEVERE, null, ex);
            }
            Motorizacao m = new Motorizacao();
            m.setSEQ_MOTORIZACAO(0);
            m.setNUM_POTENCIA(Integer.parseInt(txtPot.getText()));
            m.setNUM_VELOCIDADE_FINAL(Double.parseDouble(txtVF.getText()));
            m.setNUM_AUTONOMIA(Double.parseDouble(txtAut.getText()));
            m.setSEQ_TIPOMOTORIZACAO(i);
            m.setDSC_MOTORIZACAO(txtNome.getText().toUpperCase());
            try {
                Seq = mBus.Incluir(m);
                if (Seq > 0) {
                    JOptionPane.showMessageDialog(rootPane, "Registro: " + Seq + " incluído com sucesso!");
                    this.PreencherTabela();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Erro ao incluir");
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null,
                        ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

