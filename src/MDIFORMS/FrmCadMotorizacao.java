    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (!txtSeq.getText().isEmpty()) {
            int Seq = 0;
            if (txtSeq.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Não há registro para excluir.");
            } else {
                try {
                    Seq = mBus.Excluir(Integer.parseInt(txtSeq.getText()));
                    if (Seq > 0) {
                        JOptionPane.showMessageDialog(rootPane, "Registro excluído com sucesso!");
                        this.PreencherTabela();
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Erro ao excluir registro.");
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null,
                            ex.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um Registro para excluir");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        if (fc_verif_campos() == 1) {
            int i = 0;
            try {
                i = tmBus.fc_get_seq(cboTM.getSelectedItem().toString());
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(FrmCadMotorizacao.class.getName()).log(Level.SEVERE, null, ex);
            }
            int Seq = 0;
            if (!txtSeq.getText().isEmpty()) {
                Motorizacao m = new Motorizacao();
                m.setSEQ_MOTORIZACAO(Integer.parseInt(txtSeq.getText()));
                m.setNUM_POTENCIA(Integer.parseInt(txtPot.getText()));
                m.setNUM_VELOCIDADE_FINAL(Double.parseDouble(txtVF.getText()));
                m.setNUM_AUTONOMIA(Double.parseDouble(txtAut.getText()));
                m.setSEQ_TIPOMOTORIZACAO(i);
                m.setDSC_MOTORIZACAO(txtNome.getText().toUpperCase());
                try {
                    Seq = mBus.Alterar(m);
                    if (Seq > 0) {
                        JOptionPane.showMessageDialog(rootPane, "Registro alterado com sucesso!");
                        this.PreencherTabela();
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Erro ao atualizar.");
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null,
                            ex.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Não há registro para atualização.");
            }
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

