    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        int Seq = 0;
        int SeqА = 0;
        try {
            SeqА = aBus.fc_get_seq(cboAcessorio.getSelectedItem().toString());
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FrmCadAcessorioVeiculo.class.getName()).log(Level.SEVERE, null, ex);
        }

        AcessorioVeiculo av = new AcessorioVeiculo();
        av.setSEQ_ACESSORIOVEICULO(0);
        av.setSEQ_ACESSORIO(SeqА);
        av.setSEQ_VEICULO(Integer.parseInt(txtSeqVeiculo.getText()));

        try {
            Seq = avBus.Incluir(av);
            if (Seq > 0) {
                JOptionPane.showMessageDialog(rootPane, "Registro: " + Seq + " incluído com sucesso!");
                this.PreencherTabela();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Erro ao incluir");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,
                    ex.getMessage());
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnQueryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQueryActionPerformed
        if (!txtSeqVeiculo.getText().isEmpty()) {
            int seq = Integer.parseInt(txtSeqVeiculo.getText());
            try {
                txtNome.setText(vBus.ConsultarNome(seq));
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(FrmCadAcessorioVeiculo.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Digite um Código de Veículo para Pesquisa.");
        }
    }//GEN-LAST:event_btnQueryActionPerformed


