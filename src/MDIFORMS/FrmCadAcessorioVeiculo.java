    private void PreencherTabela()
            throws Exception {
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("Sequêncial");
        dtm.addColumn("Código Acessório");
        dtm.addColumn("Código Veículo");
        dtm.addColumn("Nome Acessório");
        try {
            ArrayList<AcessorioVeiculo> lista = avBus.Consultar();
            for (AcessorioVeiculo av : lista) {
                dtm.addRow(new String[]{
                    String.valueOf(av.getSEQ_ACESSORIOVEICULO()),
                    String.valueOf(av.getSEQ_ACESSORIO()),
                    String.valueOf(av.getSEQ_VEICULO()),
                    av.getNOM_ACESSORIO(),});
            }
            tblResult.setModel(dtm);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

    private void pCheckMenu() {
        if (frmLogin.PerfUsu != 1) {
            btnCadastrar.setEnabled(false);
            btnExcluir.setEnabled(false);
        }
    }

    private void PreencherComboAcessorio() throws ClassNotFoundException {
        DefaultComboBoxModel modelAcessorio = new DefaultComboBoxModel();
        ArrayList<Acessorio> lista = aBus.Consultar();
        for (Acessorio a : lista) {
            modelAcessorio.addElement(a.getNOM_ACESSORIO());
        }
        cboAcessorio.removeAllItems();
        cboAcessorio.setModel(modelAcessorio);
    }

