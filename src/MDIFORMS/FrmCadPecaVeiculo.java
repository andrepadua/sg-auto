    private void PreencherTabela()
            throws Exception {
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("Sequêncial");
        dtm.addColumn("Código Peça");
        dtm.addColumn("Código Veículo");
        dtm.addColumn("Nome Peça");
        try {
            ArrayList<PecaVeiculo> lista = pvBus.Consultar();
            for (PecaVeiculo pv : lista) {
                dtm.addRow(new String[]{
                    String.valueOf(pv.getSEQ_PECAVEICULO()),
                    String.valueOf(pv.getSEQ_PECA()),
                    String.valueOf(pv.getSEQ_VEICULO()),
                    pv.getNOM_PECA(),});
            }
            tblResult.setModel(dtm);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

    private void pCheckMenu() {
        if (frmLogin.PerfUsu != 1) {
            btnCadastrar.setEnabled(false);
            btnExcluir.setEnabled(false);
        }
    }

    private void PreencherComboPeca() throws ClassNotFoundException {
        DefaultComboBoxModel modelPeca = new DefaultComboBoxModel();
        ArrayList<Peca> lista = pBus.Consultar();
        for (Peca a : lista) {
            modelPeca.addElement(a.getNOM_PECA());
        }
        cboPeca.removeAllItems();
        cboPeca.setModel(modelPeca);
    }

