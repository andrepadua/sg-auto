    private void tblResultMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblResultMouseClicked
        int a = tblResult.getSelectedRow();
        txtSeq.setText(String.valueOf(tblResult.getValueAt(a, 0)));
        txtSeqVeiculo.setText(String.valueOf(tblResult.getValueAt(a, 2)));
        int seq = Integer.parseInt(txtSeqVeiculo.getText());
        try {
            txtNome.setText(vBus.ConsultarNome(seq));
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FrmCadPecaVeiculo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tblResultMouseClicked

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        int Seq = 0;
        if (!txtSeqVeiculo.getText().isEmpty()) {
            int SeqPca = 0;
            try {
                SeqPca = pBus.fc_get_seq(cboPeca.getSelectedItem().toString());
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(FrmCadPecaVeiculo.class.getName()).log(Level.SEVERE, null, ex);
            }

            PecaVeiculo pv = new PecaVeiculo();
            pv.setSEQ_PECAVEICULO(0);
            pv.setSEQ_PECA(SeqPca);
            pv.setSEQ_VEICULO(Integer.parseInt(txtSeqVeiculo.getText()));

            try {
                Seq = pvBus.Incluir(pv);
                if (Seq > 0) {
                    JOptionPane.showMessageDialog(rootPane, "Registro: " + Seq + " incluído com sucesso!");
                    this.PreencherTabela();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Erro ao incluir");
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null,
                        ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Digite um Código de Veículo para Pesquisa.");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

