    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if (fc_verif_campos() == 1) {
            int Seq = 0;
            Acessorio a = new Acessorio();
            a.setSEQ_ACESSORIO(0);
            a.setNOM_ACESSORIO(txtNome.getText());
            a.setVAL_ACESSORIO(Double.parseDouble(txtValor.getText()));
            int SeqFor = 0;
            try {
                SeqFor = fBus.fc_get_seq(cboForn.getSelectedItem().toString());
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(FrmCadAcessorios.class.getName()).log(Level.SEVERE, null, ex);
            }
            int SeqMot = 0;
            try {
                SeqMot = mBus.fc_get_seq(cboMoto.getSelectedItem().toString());
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(FrmCadAcessorios.class.getName()).log(Level.SEVERE, null, ex);
            }
            a.setSEQ_FORNECEDOR(SeqFor);
            a.setSEQ_MOTORIZACAO(SeqMot);
            try {
                Seq = aBus.Incluir(a);
                if (Seq > 0) {
                    JOptionPane.showMessageDialog(rootPane, "Registro: " + Seq + " incluído com sucesso!");
                    this.PreencherTabela();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Erro ao incluir");
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null,
                        ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Verifique campos nulos!");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        if (fc_verif_campos() == 1) {
            int Seq = 0;
            if (!txtSeq.getText().isEmpty()) {
                Acessorio a = new Acessorio();
                a.setSEQ_ACESSORIO(Integer.parseInt(txtSeq.getText()));
                a.setNOM_ACESSORIO(txtNome.getText());
                a.setVAL_ACESSORIO(Double.parseDouble(txtValor.getText()));
                int SeqFor = 0;
                try {
                    SeqFor = fBus.fc_get_seq(cboForn.getSelectedItem().toString());
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(FrmCadAcessorios.class.getName()).log(Level.SEVERE, null, ex);
                }
                int SeqMot = 0;
                try {
                    SeqMot = mBus.fc_get_seq(cboMoto.getSelectedItem().toString());
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(FrmCadAcessorios.class.getName()).log(Level.SEVERE, null, ex);
                }
                a.setSEQ_FORNECEDOR(SeqFor);
                a.setSEQ_MOTORIZACAO(SeqMot);
                try {
                    Seq = aBus.Alterar(a);
                    if (Seq > 0) {
                        JOptionPane.showMessageDialog(rootPane, "Registro alterado com sucesso!");
                        this.PreencherTabela();
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Erro ao atualizar.");
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null,
                            ex.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Não há registro para atualização.");
            }
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (!txtSeq.getText().isEmpty()) {
            int Seq = 0;
            if (txtSeq.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Não há registro para excluir.");
            } else {
                try {
                    Seq = aBus.Excluir(Integer.parseInt(txtSeq.getText()));
                    if (Seq > 0) {
                        JOptionPane.showMessageDialog(rootPane, "Registro excluído com sucesso!");
                        this.PreencherTabela();
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Erro ao excluir registro.");
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null,
                            ex.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um Registro para excluir");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

