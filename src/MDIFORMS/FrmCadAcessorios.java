    private void PreencherTabela()
            throws Exception {
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("Código");
        dtm.addColumn("Nome");
        dtm.addColumn("Valor");
        dtm.addColumn("Motorização");
        dtm.addColumn("Fornecedor");
        try {
            ArrayList<Acessorio> lista = aBus.Consultar();
            for (Acessorio a : lista) {
                dtm.addRow(new String[]{
                    String.valueOf(a.getSEQ_ACESSORIO()),
                    a.getNOM_ACESSORIO(),
                    String.valueOf(a.getVAL_ACESSORIO()),
                    a.getDSC_MOTORIZACAO(),
                    a.getNOM_FORNECEDOR()
                });
            }
            tblResult.setModel(dtm);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

    private void PreencherComboForn() throws ClassNotFoundException {
        DefaultComboBoxModel modelForn = new DefaultComboBoxModel();
        ArrayList<Fornecedor> lista = fBus.Consultar();
        for (Fornecedor f : lista) {
            modelForn.addElement(f.getNOM_FORNECEDOR());
        }
        cboForn.removeAllItems();
        cboForn.setModel(modelForn);
    }

    private void PreencherComboMoto() throws ClassNotFoundException {
        DefaultComboBoxModel modelMoto = new DefaultComboBoxModel();
        ArrayList<Motorizacao> lista = mBus.Consultar();
        for (Motorizacao m : lista) {
            modelMoto.addElement(m.getDSC_MOTORIZACAO());
        }
        cboMoto.removeAllItems();
        cboMoto.setModel(modelMoto);
    }
    
