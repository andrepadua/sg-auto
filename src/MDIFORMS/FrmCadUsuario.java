    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (!txtSeq.getText().isEmpty()) {
            int Seq = 0;
            if (txtSeq.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Não há registro para excluir.");
            } else {
                try {
                    Seq = uBus.Excluir(Integer.parseInt(txtSeq.getText()));
                    if (Seq > 0) {
                        JOptionPane.showMessageDialog(rootPane, "Registro excluído com sucesso!");
                        this.PreencherTabela();
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Erro ao excluir registro.");
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null,
                            ex.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um Registro para excluir");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        if (fc_verif_campos() == 1) {
            int i = 0;
            try {
                i = pBus.fc_get_seq(cboPerfil.getSelectedItem().toString());
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(FrmCadUsuario.class.getName()).log(Level.SEVERE, null, ex);
            }
            int Seq = 0;
            if (!txtSeq.getText().isEmpty()) {
                Usuario u = new Usuario();
                u.setSEQ_USUARIO(Integer.parseInt(txtSeq.getText()));
                u.setNOM_USUARIO(txtNome.getText());
                u.setLOGINNAME(txtLogin.getText().toUpperCase());
                u.setSEQ_PERFIL(i);

                try {
                    Seq = uBus.Alterar(u);
                    if (Seq > 0) {
                        JOptionPane.showMessageDialog(rootPane, "Registro alterado com sucesso!");
                        this.PreencherTabela();
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Erro ao atualizar.");
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null,
                            ex.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Não há registro para atualização.");
            }
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if (fc_verif_campos() == 1) {
            int Seq = 0;
            int i = 0;
            try {
                i = pBus.fc_get_seq(cboPerfil.getSelectedItem().toString());
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(FrmCadUsuario.class.getName()).log(Level.SEVERE, null, ex);
            }
            Usuario u = new Usuario();
            u.setSEQ_USUARIO(0);
            u.setNOM_USUARIO(txtNome.getText());
            u.setLOGINNAME(txtLogin.getText().toUpperCase());
            u.setSEQ_PERFIL(i);
            try {
                Seq = uBus.Incluir(u);
                if (Seq > 0) {
                    JOptionPane.showMessageDialog(rootPane, "Registro: " + Seq + " incluído com sucesso!");
                    this.PreencherTabela();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Erro ao incluir");
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null,
                        ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private int fc_verif_campos() {
        if (txtNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Não possui valor para Nome. Favor Preecher!");
            return 0;
        }
        if (txtLogin.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Não possui valor para Nome. Favor Preecher!");
            return 0;
        }
        return 1;
    }

