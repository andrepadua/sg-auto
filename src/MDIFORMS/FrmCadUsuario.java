    private void PreencherTabela()
            throws Exception {
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("Cod. Usuário");
        dtm.addColumn("Nome Usuário");
        dtm.addColumn("Login");
        dtm.addColumn("Perfil");
        try {
            ArrayList<Usuario> lista = uBus.Consultar();
            for (Usuario u : lista) {
                dtm.addRow(new String[]{
                    String.valueOf(u.getSEQ_USUARIO()),
                    u.getNOM_USUARIO(),
                    u.getLOGINNAME(),
                    u.getNOM_PERFIL()
                });
            }
            tblResult.setModel(dtm);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

    private void PreencherCombo() throws ClassNotFoundException {
        DefaultComboBoxModel modelPerfil = new DefaultComboBoxModel();
        ArrayList<Perfil> lista = pBus.Consultar();
        for (Perfil p : lista) {
            modelPerfil.addElement(p.getNOM_PERFIL());
        }
        cboPerfil.removeAllItems();
        cboPerfil.setModel(modelPerfil);
    }

