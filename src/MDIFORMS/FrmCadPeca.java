    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if (fc_verif_campos() == 1) {
            int Seq = 0;
            int Flg_Prod_Mont = 0;
            if (cboPM.getSelectedItem().toString().equals("Sim")) {
                Flg_Prod_Mont = 1;
            }
            int SeqFor = 0;
            try {
                SeqFor = fBus.fc_get_seq(cboForn.getSelectedItem().toString());
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(FrmCadPeca.class.getName()).log(Level.SEVERE, null, ex);
            }
            Peca p = new Peca();
            p.setSEQ_PECA(0);
            p.setNOM_PECA(txtNome.getText().toUpperCase());
            p.setSEQ_FORNECEDOR(SeqFor);
            p.setFLG_PROD_MONT(Flg_Prod_Mont);
            try {
                Seq = pBus.Incluir(p);
                if (Seq > 0) {
                    JOptionPane.showMessageDialog(rootPane, "Registro: " + Seq + " incluído com sucesso!");
                    this.PreencherTabela();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Erro ao incluir");
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null,
                        ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

