    private void PreencherTabela()
            throws Exception {
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("Código");
        dtm.addColumn("Nome");
        dtm.addColumn("Fornecedor");
        dtm.addColumn("Prod. na Montadora");
        try {
            ArrayList<Peca> lista = pBus.Consultar();
            for (Peca p : lista) {
                dtm.addRow(new String[]{
                    String.valueOf(p.getSEQ_PECA()),
                    p.getNOM_PECA(),
                    p.getNOM_FORNECEDOR(),
                    p.getPROD_MONTADORA()
                });
            }
            tblResult.setModel(dtm);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

    private void pCheckMenu() {
        if (frmLogin.PerfUsu != 1) {
            btnCadastrar.setEnabled(false);
            btnAtualizar.setEnabled(false);
            btnExcluir.setEnabled(false);
        }
    }

    private void PreencherComboForn() throws ClassNotFoundException {
        DefaultComboBoxModel modelForn = new DefaultComboBoxModel();
        ArrayList<Fornecedor> lista = fBus.Consultar();
        for (Fornecedor f : lista) {
            modelForn.addElement(f.getNOM_FORNECEDOR());
        }
        cboForn.removeAllItems();
        cboForn.setModel(modelForn);
    }

    private void PreencherComboPM() {
        DefaultComboBoxModel modelCategoria = new DefaultComboBoxModel();
        modelCategoria.addElement("Sim");
        modelCategoria.addElement("Não");
        cboPM.removeAllItems();
        cboPM.setModel(modelCategoria);
    }

